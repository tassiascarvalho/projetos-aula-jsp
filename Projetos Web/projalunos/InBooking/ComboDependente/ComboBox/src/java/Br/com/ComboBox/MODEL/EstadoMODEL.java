package Br.com.ComboBox.MODEL;


public class EstadoMODEL {
    private Integer Cod_Estado;
    private String Nome_Estado;
    private String Sigla_Estado;

    /**
     * @return the Cod_Estado
     */
    public Integer getCod_Estado() {
        return Cod_Estado;
    }

    /**
     * @param Cod_Estado the Cod_Estado to set
     */
    public void setCod_Estado(Integer Cod_Estado) {
        this.Cod_Estado = Cod_Estado;
    }

    /**
     * @return the Nome_Estado
     */
    public String getNome_Estado() {
        return Nome_Estado;
    }

    /**
     * @param Nome_Estado the Nome_Estado to set
     */
    public void setNome_Estado(String Nome_Estado) {
        this.Nome_Estado = Nome_Estado;
    }

    /**
     * @return the Sigla_Estado
     */
    public String getSigla_Estado() {
        return Sigla_Estado;
    }

    /**
     * @param Sigla_Estado the Sigla_Estado to set
     */
    public void setSigla_Estado(String Sigla_Estado) {
        this.Sigla_Estado = Sigla_Estado;
    }
    
    
}
